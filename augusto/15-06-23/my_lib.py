""" a lib to calculate the area and perimeter """
import math

def math_area_of_circle(radius):
    """ calculate the area from a radius """
    return math.pi * radius * radius

def math_perimeter_of_circle(radius):
    """ calculate the perimeter from a radius """
    return 2 * math.pi * radius

def math_area_of_square(side):
    """ calculate the area of a square """
    return side * side

def math_perimeter_of_square(side):
    """ calculate the perimeter of a square """
    return 4 * side
  